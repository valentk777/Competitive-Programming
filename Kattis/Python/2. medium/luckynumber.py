# -------------------------------------------------------Solution-------------------------------------------------------

def solve():
    n = iinp()

    # initially all numbers are divisible by 1
    ans = list(range(1, 10))

    for i in range(2, n + 1):
        numbers = []

        # iterate all candidates and add new digit at the end
        for number in ans:
            for digit in range(10):
                candidate = number * 10 + digit

                if candidate % i == 0:
                    numbers.append(candidate)

        ans = numbers

    return len(ans)


def run():
    print(solve())


