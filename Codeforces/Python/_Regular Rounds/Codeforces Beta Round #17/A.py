def solve():
    n, k = intl()
    ans = 0

    for a, b in zip(all_primes[:-1], all_primes[1:]):
        candidate = a + b + 1

        if candidate > n:
            break

        if candidate in all_primes:
            ans += 1

    if ans >= k:
        return "YES"

    return "NO"


