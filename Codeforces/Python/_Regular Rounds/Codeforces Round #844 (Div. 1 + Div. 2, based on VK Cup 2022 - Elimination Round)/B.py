def solve():
    n = iinp()
    a = sorted(intl())

    total = 0
    new_a = []

    # count all that must go
    for i in range(n):
        if total >= a[i]:
            total += 1
        else:
            new_a.append(a[i])

    a = new_a
    n = len(a)

    if n == 0:
        return 1

    # at this point all can go or no go.
    # So let's add current state with all candidates picking "no go" option and all candidates picking "go" option.
    ans = 2

    # let's identify if there is more options.
    # we will have more options if we find finite state where part can go and others are not effected

    i = 0
    while i < n:
        # from now, we force to go all of them until state become valid. at that point we find another option

        # add current
        total += 1

        # all same numbers will go together
        j = i + 1
        while j < n and a[i] == a[j]:
            j += 1
            total += 1

        i = j

        if i < n and total - 1 >= a[i - 1] and total < a[i]:  # -1 because "not counting myself"
            # we found a valid state.
            ans += 1

    return ans


def run():
    t = iinp()

    for _ in range(t):
        print(solve())


